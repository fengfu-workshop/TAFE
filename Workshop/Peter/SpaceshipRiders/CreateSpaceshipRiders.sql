CREATE TABLE STAR (
	Star_No		int NOT NULL,
	Name		NVARCHAR(50),
	SpectraType NVARCHAR(50),
	Position	NVARCHAR(50),
	CONSTRAINT PK_STAR PRIMARY KEY (Star_No)
)
GO

CREATE TABLE PLANET (
	Planet_No	int	NOT NULL,
	Name		NVARCHAR(50),
	DayLength	int,
	Atmosphere	NVARCHAR(50),
	Technology	NVARCHAR(50),
	Star_No		int

	CONSTRAINT PK_PLANET PRIMARY KEY (Planet_No), 
	CONSTRAINT FK_PLANET_STAR FOREIGN KEY (Star_No) REFERENCES STAR
)
GO

CREATE TABLE SPACESHIP (
	ShipRego_No			int	NOT NULL,
	ShipName			nvarchar(50),
	LightYearsTravelled	nvarchar(50),
	MaxWrapSpeed		int

	CONSTRAINT PK_SPACESHIP PRIMARY KEY (ShipRego_No)
)
GO

CREATE TABLE RAID (
	Raid_No		int	NOT NULL,
	ShipRego_No	int,
	Planet_No	int,
	Date		datetime,
	StartTime	datetime,
	EndTime		datetime,
	OppositionLevel nvarchar(50)

	CONSTRAINT PK_RAID PRIMARY KEY (Raid_No), 
	CONSTRAINT FK_RAID_PLANET FOREIGN KEY (Planet_No) REFERENCES PLANET,
	CONSTRAINT FK_RAID_SPACESHIP FOREIGN KEY (ShipRego_No) REFERENCES SPACESHIP
)
GO

CREATE TABLE PRISONER (
	Prisoner_No		int	NOT NULL,
	FirstName		nvarchar(50),
	LastName		nvarchar(50),
	YearOfBirth		int,
	YearOfDeath		int,
	Raid_No			int,
	RansomPaid		money,
	Colour			nvarchar(50)

	CONSTRAINT PK_PRISONER PRIMARY KEY (Prisoner_No), 
	CONSTRAINT FK_PRISONER_RAID FOREIGN KEY (Raid_No) REFERENCES RAID
)
GO

CREATE TABLE POSITION (
	PositionName	nvarchar(50) NOT NULL,
	Description		text

	CONSTRAINT PK_POSITION PRIMARY KEY (PositionName)
)
GO

CREATE TABLE MEDAL (
	Medal		nvarchar(50) NOT NULL,
	Value		money

	CONSTRAINT PK_MEDAL PRIMARY KEY (Medal)
)
GO

CREATE TABLE TWEEDLEDEER (
	Tweedle_No		int	NOT NULL,
	FirstName		nvarchar(50),
	LastName		nvarchar(50),
	Birth_Date		datetime,
	DeathDate		datetime,
	Gender			nvarchar(50),
	Position		nvarchar(50),
	Father			int,
	Mother			int

	CONSTRAINT PK_TWEEDLEDEER PRIMARY KEY (Tweedle_No), 
	CONSTRAINT FK_FATHER_TWEEDLEDEER FOREIGN KEY (Father) REFERENCES TWEEDLEDEER,
	CONSTRAINT FK_MOTHER_TWEEDLEDEER FOREIGN KEY (Mother) REFERENCES TWEEDLEDEER
)
GO

CREATE TABLE SHIP_ROSTER (
	ShipRego_No			int	NOT NULL,
	Tweedle_No			int NOT NULL,
	CommencementDate	datetime NOT NULL,
	TerminationDate		datetime,
	Medal				nvarchar(50)

	CONSTRAINT PK_SHIP_ROSTER PRIMARY KEY (ShipRego_No, Tweedle_No, CommencementDate), 
	CONSTRAINT FK_SHIP_ROSTER_MEDAL FOREIGN KEY (Medal) REFERENCES MEDAL,
	CONSTRAINT FK_SHIP_ROSTER_TWEEDLEDEER FOREIGN KEY (Tweedle_No) REFERENCES TWEEDLEDEER,
	CONSTRAINT FK_SHIP_ROSTER_SPACESHIP FOREIGN KEY (ShipRego_No) REFERENCES SPACESHIP,

)
GO

